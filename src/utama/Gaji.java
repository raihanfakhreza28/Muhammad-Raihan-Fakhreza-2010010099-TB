    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       int c = JOptionPane.showConfirmDialog(null, "Apakah anda ingin mengubah data "+idtransaksi.getText()+" ?" ,"Informasi", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION){
            if (idtransaksi.getText().equals("") || namapegawai.getText().equals("") || namajabatan.getText().equals("") || tunjangan.getText().equals("") || gajipokok.getText().equals("") || totalgaji.getText().equals("") ){
                JOptionPane.showMessageDialog(null, "Data Wajib diisi..!");
            } else {
                try {
                    int s;
                    String sql = "UPDATE tbl_gaji SET id_pegawai = '"+namapegawai.getText()+"', id_jabatan = '"+namajabatan.getText()+"' ,  tunjangan = '"+tunjangan.getText()+"', gaji_pokok = '"+gajipokok.getText()+"' WHERE id_transaksi = '"+idtransaksi.getText()+"' ";
                    st = conn.createStatement();
                    s = st.executeUpdate(sql);
                    if (s == 1){
                        JOptionPane.showMessageDialog(null, "Berhasil Diubah");
                        clear();
                        tampilan();
                    }
                } catch (Exception e){
                    System.out.println(e.toString());
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

