    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int c = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data "+idtransaksi.getText()+"?","Warning", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION){
            try {
                int s;
                String sql;
                sql = "DELETE FROM tbl_gaji WHERE id_transaksi = '"+idtransaksi.getText()+"' ";
                st = conn.createStatement();
                s = st.executeUpdate(sql);
                if (s == 1){
                    JOptionPane.showMessageDialog(null, "Berhasil dihapus");
                    clear();
                    tampilan();
                }
            } catch (Exception e){
                    System.out.println(e.toString());
            } 
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void buttonsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonsimpanActionPerformed
 
        if (idtransaksi.getText().equals("") || namapegawai.getText().equals("") || namajabatan.getText().equals("") || tunjangan.getText().equals("") || gajipokok.getText().equals("") || totalgaji.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Data Wajib diisi..!");
        } else {
            try {
                int s;
                String sql = "INSERT INTO tbl_gaji VALUES ('"+idtransaksi.getText()+"', '"+namapegawai.getText()+"', '"+namajabatan.getText()+"', '"+tunjangan.getText()+"', '"+gajipokok.getText()+"', '"+totalgaji.getText()+"')";
                st = conn.createStatement();
                s = st.executeUpdate(sql);
                if (s == 1){
                    JOptionPane.showMessageDialog(null, "Data Berhasil ditambahkan");
                    clear();
                    tampilan();
                }
            } catch (HeadlessException | SQLException e){
                System.out.println(e.toString());
            }
        }
    }//GEN-LAST:event_buttonsimpanActionPerformed

