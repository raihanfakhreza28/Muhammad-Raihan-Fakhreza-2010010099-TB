    public void tampilan(){
        Object [] baris = {"ID Jabatan","Nama Jabatan",};
        tabMode = new DefaultTableModel(null, baris);
        tablejabatan.setModel(tabMode);
        try {
                String sql = "SELECT * FROM tbl_jabatan";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                int no = 0;
                while (rs.next()){
                        no++;
                        String idj   = rs.getString("id_jabatan");
                        String nj    = rs.getString("nama_jabatan");

                        Object [] data = {idj,nj};
                        tabMode.addRow(data);
                }
        } catch (SQLException e){
                System.out.println(e.toString());
        }
    }
    
