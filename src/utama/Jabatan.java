    private void buttoneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttoneditActionPerformed
        int c = JOptionPane.showConfirmDialog(null, "Apakah anda ingin mengubah data "+Tidjabatan.getText()+" ?" ,"Informasi", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION){
            if (Tidjabatan.getText().equals("") || Tnamajabatan.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Data Wajib diisi..!");
            } else {
                try {
                    int s;
                    String sql = "UPDATE tbl_jabatan SET nama_jabatan = '"+Tnamajabatan.getText()+"' WHERE id_jabatan = '"+Tidjabatan.getText()+"' ";
                    st = conn.createStatement();
                    s = st.executeUpdate(sql);
                    if (s == 1){
                        JOptionPane.showMessageDialog(null, " Data Berhasil Diubah");
                        clear();
                        tampilan();
                    }
                } catch (Exception e){
                    System.out.println(e.toString());
                }
            }
        }
    }//GEN-LAST:event_buttoneditActionPerformed

