    public void tampilan(){
        Object [] baris = {"ID Pegawai","Nama Pegawai", "Tanggal Lahir", "Alamat", "Nomor Telepon",};
        tabMode = new DefaultTableModel(null, baris);
        tablepegawai.setModel(tabMode);
        try {
                String sql = "SELECT * FROM tbl_pegawai";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                int no = 0;
                while (rs.next()){
                        no++;
                        String idp   = rs.getString("id_pegawai");
                        String np    = rs.getString("nama_pegawai");
                        String tl    = rs.getString("tgl_lahir");
                        String a     = rs.getString("alamat");
                        String n     = rs.getString("no_telp");

                        Object [] data = {idp,np,tl,a,n};
                        tabMode.addRow(data);
                }
        } catch (SQLException e){
                System.out.println(e.toString());
        }
    }
    
