    private void buttoneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttoneditActionPerformed
        String hari = cmbhari.getSelectedItem().toString();
        String bulan = cmbbulan.getSelectedItem().toString();
        String tahun = cmbtahun.getSelectedItem().toString();
        String tanggal = tahun+"-"+bulan+"-"+hari;

        int c = JOptionPane.showConfirmDialog(null, "Apakah anda ingin mengubah data "+TID.getText()+" ?" ,"Informasi", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION){
            if (TID.getText().equals("") || Tnamapegawai.getText().equals("") || tanggal == "" || Talamat.getText().equals("") || Tnomortelepon.getText().equals("") ){
                JOptionPane.showMessageDialog(null, "Data Wajib diisi..!");
            } else {
                try {
                    int s;
                    String sql = "UPDATE tbl_pegawai SET nama_pegawai = '"+Tnamapegawai.getText()+"', tgl_lahir = '"+tanggal+"',  alamat = '"+Talamat.getText()+"', no_telp = '"+Tnomortelepon.getText()+"' WHERE id_pegawai = '"+TID.getText()+"' ";
                    st = conn.createStatement();
                    s = st.executeUpdate(sql);
                    if (s == 1){
                        JOptionPane.showMessageDialog(null, "Berhasil Diubah");
                        clear();
                        tampilan();
                    }
                } catch (Exception e){
                    System.out.println(e.toString());
                }
            }
        }
    }//GEN-LAST:event_buttoneditActionPerformed

    private void buttonsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonsimpanActionPerformed
        String hari = cmbhari.getSelectedItem().toString();
        String bulan = cmbbulan.getSelectedItem().toString();
        String tahun = cmbtahun.getSelectedItem().toString();
        String tanggal = tahun+"-"+bulan+"-"+hari;

        if (TID.getText().equals("") || Tnamapegawai.getText().equals("") || "".equals(tanggal) || Talamat.getText().equals("") || Tnomortelepon.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Data Wajib diisi..!");
        } else {
            try {
                int s;
                String sql = "INSERT INTO tbl_pegawai VALUES ('"+TID.getText()+"', '"+Tnamapegawai.getText()+"', '"+tanggal+"', '"+Talamat.getText()+"', '"+Tnomortelepon.getText()+"')";
                st = conn.createStatement();
                s = st.executeUpdate(sql);
                if (s == 1){
                    JOptionPane.showMessageDialog(null, "Data Berhasil ditambahkan");
                    clear();
                    tampilan();
                }
            } catch (HeadlessException | SQLException e){
                System.out.println(e.toString());
            }
        }
    }//GEN-LAST:event_buttonsimpanActionPerformed

